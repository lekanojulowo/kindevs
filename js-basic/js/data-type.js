// new let
let message;

//Number : int, float
message = 25
console.log(message)

// Boolean: true or false
message = true
console.log(message)

// Array collection
message = ['Olamide', 'Inioluwa']
console.log(message)
console.log(message[0])
console.log(message[1])

// Literal Object Declaration
message = { firstName: 'Olamide', surname: 'Akerele' }
console.log(message)

// Dot Notation
console.log(message.firstName)
console.log(message.surname)

// Bracket Notation
console.log(message["firstName"])
console.log(message["surname"])

